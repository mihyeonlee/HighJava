package goRail.vo;

public class MileageVO {
	private String mil_no;
	private int mil_update;
	private int mil_sum;
	private String mil_date;
	private String mil_type;
	private int mem_no;
	public String getMil_no() {
		return mil_no;
	}
	public void setMil_no(String mil_no) {
		this.mil_no = mil_no;
	}
	public int getMil_update() {
		return mil_update;
	}
	public void setMil_update(int mil_update) {
		this.mil_update = mil_update;
	}
	public int getMil_sum() {
		return mil_sum;
	}
	public void setMil_sum(int mil_sum) {
		this.mil_sum = mil_sum;
	}
	public String getMil_date() {
		return mil_date;
	}
	public void setMil_date(String mil_date) {
		this.mil_date = mil_date;
	}
	public String getMil_type() {
		return mil_type;
	}
	public void setMil_type(String mil_type) {
		this.mil_type = mil_type;
	}
	public int getMem_no() {
		return mem_no;
	}
	public void setMem_no(int mem_no) {
		this.mem_no = mem_no;
	}
	
	


}
