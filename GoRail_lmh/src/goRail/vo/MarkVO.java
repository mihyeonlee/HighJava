package goRail.vo;

public class MarkVO {
	private int mark_no;
	private String mark_sst;
	private String mark_dst;
	private int mem_no;
	public int getMark_no() {
		return mark_no;
	}
	public void setMark_no(int mark_no) {
		this.mark_no = mark_no;
	}
	public String getMark_sst() {
		return mark_sst;
	}
	public void setMark_sst(String mark_sst) {
		this.mark_sst = mark_sst;
	}
	public String getMark_dst() {
		return mark_dst;
	}
	public void setMark_dst(String mark_dst) {
		this.mark_dst = mark_dst;
	}
	public int getMem_no() {
		return mem_no;
	}
	public void setMem_no(int mem_no) {
		this.mem_no = mem_no;
	}
	
	
	
}
