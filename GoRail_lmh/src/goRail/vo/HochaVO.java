package goRail.vo;

public class HochaVO {
	private String hocha_no;
	private String train_no;
	private int hocha_seat;
	public String getHocha_no() {
		return hocha_no;
	}
	public void setHocha_no(String hocha_no) {
		this.hocha_no = hocha_no;
	}
	public String getTrain_no() {
		return train_no;
	}
	public void setTrain_no(String train_no) {
		this.train_no = train_no;
	}
	public int getHocha_seat() {
		return hocha_seat;
	}
	public void setHocha_seat(int hocha_seat) {
		this.hocha_seat = hocha_seat;
	}
	
	
	

}
